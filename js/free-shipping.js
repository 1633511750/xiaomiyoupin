window.onload = function () {
  var freeShippingUls = $('.free-shipping ul');
  // 9.9包邮模块的ul
  var freeShippingUl0 = freeShippingUls[0];
  // 19.9包邮模块的ul
  var freeShippingUl1 = freeShippingUls[1];
  // 29.9包邮模块的ul
  var freeShippingUl2 = freeShippingUls[2];
  // 49.9包邮模块的ul
  var freeShippingUl3 = freeShippingUls[3];
  var liTemplateQi = "起";
  // 新人首单全额返模块的ul
  var freeShippingUL4 = freeShippingUls[4];

  // 9.9
  drawGoodsList(goods_list0, freeShippingUl0);
  // 19.9
  drawGoodsList(goods_list1, freeShippingUl1);
  // 29.9
  drawGoodsList(goods_list2, freeShippingUl2);
  // 49.9
  drawGoodsList(goods_list3, freeShippingUl3);
  // 新人首单全额返
  drawListData(list_data, freeShippingUL4);

  // 渲染商品数据到页面
  function drawGoodsList(goods_list, freeShippingUl) {
    var len = goods_list.length % 3 !== 0 ? goods_list.length - goods_list.length % 3 : goods_list.length;
    for (let i = 0; i < len; i++) {
      var liTemplate = `
        <li class="item fs12">
          <img
            src="${goods_list[i].pic_url}"
            alt="">
          <div class="info">${goods_list[i].name}</div>
          <div class="new-person-price">新人价</div>
          <div class="price-wrap">
            <span class="red">￥<span class="price">${(goods_list[i].price_min / 100).toFixed(1)}</span><span class="qi">${liTemplateQi}</span></span>
            <span class="price-del">${(goods_list[i].market_price / 100).toFixed(1)}</span>
          </div>
        </li>
      `;

      var li = document.createElement('li');
      li.innerHTML = liTemplate;
      li = li.children[0];
      if (goods_list[i].good_comment_percents > 95) {
        var commentDiv = document.createElement('div');
        commentDiv.innerHTML = `<div class="comment fs12">好评率${goods_list[i].good_comment_percents}%</div>`;
        commentDiv = commentDiv.children[0];
        li.appendChild(commentDiv);
      }
      freeShippingUl.appendChild(li);
    }
  }

  // 渲染新人首单全额返数据
  function drawListData(list, ul) {
    for (let i = 0; i < list.length; i++) {
      var price = (list[i].price_min / 100).toFixed(1) + '';
      price = price[price.length - 1] === '0' ? price.slice(0, price.length - 2) : price;
      var liTemplate = `
        <li class="fan-item">
          <img
            src="${list[i].img800s}"
            alt="">
          <div class="name">${list[i].attr_ext.custom_name}</div>
          <div class="desc">${list[i].summary}</div>
          <div class="price-wrap">
            <span class="symbol">￥</span><span class="price">${price}</span>
            <div class="all-fan">全额返</div>
          </div>
        </li>
      `;

      var li = document.createElement('li');
      li.innerHTML = liTemplate;
      li = li.children[0];
      ul.appendChild(li);
    }
  }

  // 轮播图
  var picIndex = 0;
  var touchMoveX = 0;
  var picUl = $('.lbt-wrap ul')[0];
  var picLis = $('.lbt-wrap li');
  var dds = $('.lbt-wrap dd');

  function nextPic() {
    picUl.style.transition = 'none';
    picLis[picIndex + 2].classList.remove('active');
    if (picIndex === picLis.length - 5) {
      picIndex = -1;
      picUl.style.left = px_to_rem(touchMoveX) + 'rem';
      picUl.offsetLeft;
    }
    picUl.offsetLeft;
    picUl.style.transition = 'left 0.3s';
    picIndex++;
    picLis[picIndex + 2].classList.add('active');
    picUl.style.left = -1.13 * (picIndex + 1) + 'rem';
    touchMoveX = 0;
  }

  function prevPic() {
    picUl.style.transition = 'none';
    picLis[picIndex + 2].classList.remove('active');
    if (picIndex === 0) {
      picIndex = picLis.length - 4;
      picUl.style.left = -1.13 * (picLis.length - 3) + px_to_rem(touchMoveX) + 'rem';
    }
    picUl.offsetLeft;
    picUl.style.transition = 'left 0.3s';
    picIndex--;
    picLis[picIndex + 2].classList.add('active');
    picUl.style.left = -1.13 * (picIndex + 1) + 'rem';
  }

  function curPic() {
    picUl.style.transition = 'left 0.3s';
    picUl.style.left = -1.13 * (picIndex + 1) + 'rem';
  }

  ontransitionend = function () {
    for (let i = 0; i < dds.length; i++) {
      dds[i].classList.remove('active');
    }
    dds[getImgIndex()].classList.add('active');
  }

  var lbtTimer = setInterval(() => {
    nextPic();
  }, 2000);

  function getImgIndex() {
    if (picIndex === 5) {
      return 0;
    }
    return picIndex;
  }

  // 触摸事件
  var touchBeginX;
  var picUlLeft;
  picUl.ontouchstart = function (e) {
    clearInterval(lbtTimer);
    this.style.transition = 'none';
    touchBeginX = e.targetTouches[0].clientX;
    picUlLeft = picUl.offsetLeft;
  }
  picUl.ontouchmove = function (e) {
    touchMoveX = e.changedTouches[0].clientX - touchBeginX;
    touchMoveX = touchMoveX > rem_to_px(1.13) ? rem_to_px(1.13) : touchMoveX;
    touchMoveX = touchMoveX < rem_to_px(-1.13) ? rem_to_px(-1.13) : touchMoveX;
    picUl.style.left = (picUlLeft + touchMoveX) + 'px';
  }
  picUl.ontouchend = function (e) {
    // this.style.transition = 'left 0.3s';
    touchBeginX = e.changedTouches[0].clientX - touchBeginX;
    if (touchBeginX >= rem_to_px(0.40)) {
      prevPic();
    } else if (touchBeginX <= rem_to_px(-0.40)) {
      nextPic();
    } else {
      curPic();
    }
    lbtTimer = setInterval(() => {
      nextPic();
    }, 2000);
  }

  // 切换楼层
  var navContentUl = $('nav .content ul')[0];
  navContentUl.ontouchstart = function (e) {
    this.baseX = e.targetTouches[0].clientX;
    this.leftX = this.offsetLeft;
  }
  navContentUl.ontouchmove = function (e) {
    var left = this.leftX + e.targetTouches[0].clientX - this.baseX;
    left = left > 0 ? 0 : left;
    left = left < rem_to_px(-2.30) ? rem_to_px(-2.30) : left;
    this.style.left = left + 'px';
  }

  var navContentLis = $('nav .content li');
  var navToggleBtn = $('nav .right')[0];
  var navContent = $('nav .content')[0];
  var navToggle = $('nav .toggle')[0];
  var navImg = $('nav .right img')[0];
  var navToggleLis = $('nav .toggle li');
  var navState = 0;
  navToggleBtn.onclick = function () {
    if (navState === 0) {
      navContent.style.display = 'none';
      navToggle.style.display = 'block';
      navImg.style.transform = 'rotate(180deg)';
      navState = 1;
    } else {
      navContent.style.display = 'block';
      navToggle.style.display = 'none';
      navImg.style.transform = 'rotate(0deg)';
      navState = 0;
    }
  }

  var mustBuyWrap = $('.must-buy-wrap')[0];
  var freeShippings = $('.free-shipping');
  for (let i = 0; i < navToggleLis.length; i++) {
    navContentLis[i].onclick = navToggleLis[i].onclick = function () {
      if (i === 0) {
        scroll(0, mustBuyWrap.offsetTop - 1.00);
      } else {
        scroll(0, freeShippings[i - 1].offsetTop - 1.00);
      }
    }
  }
}

//rem 转成 px
function rem_to_px(rem) {
  return (rem / 375 * document.documentElement.clientWidth * 100).toFixed(4) - 0;
}

// px转成rem
function px_to_rem(px) {
  return (px * 375 / document.documentElement.clientWidth / 100).toFixed(4) - 0;
}


function $(type) {
  if (type[0] === '#') {
    return document.querySelector(type);
  } else {
    return document.querySelectorAll(type);
  }
}
